     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function showEpisodes($serieId, $seasonNumber)
    {
        $episodes = Episode::where('serie_id', $serieId)->where('season_number', $seasonNumber)->get();
        $serie = Serie::where('id', $serieId)->first();
        $season = Season::where('serie_id', $serieId)->where('season_number', $seasonNumber)->first();

        return view('seasons.show', [
            'episodes' => $episodes,
            'serie' => $serie,
            'season' => $season
        ]);
    }

